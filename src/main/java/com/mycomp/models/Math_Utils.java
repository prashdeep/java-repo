package com.mycomp.models;

public class Math_Utils {

	/**
	 * Given two integers return the sum of both
	 * @param a
	 * @param b
	 * @return a + b
	 */
	public int add (int a, int b) {
		return a + b;
	}
	
	/**
	 * Given two integers return the substraction
	 * @param a
	 * @param b
	 * @return a - b
	 */
	public int substract (int a, int  b) {
		return a - b;
	}
}
